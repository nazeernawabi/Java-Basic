package basics;

public class IncrementPractice {

	public static void main(String[] args) {
		
		
		int a;
		double b;
		
		a = 35;
		b = -21.4;
		
		a ++;
		System.out.println(a);
		a --;
		a --;
		System.out.println(a);
		b ++;
		System.out.println(b);
		b --;
		b --;
		System.out.println(b);
		
		
		
		

	}

}
